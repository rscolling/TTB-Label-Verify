def worksheet_rows(page):
    return page.locator("#worksheet-body > tr.worksheet-row")


def wait_for_banner(page, timeout: int = 15_000):
    playwright_api.expect(page.locator("#banner-text")).to_contain_text(
        "scanned", timeout=timeout
    )


def wait_for_ingest(page, row_word: str = "row"):
    playwright_api.expect(page.locator("#csv-status")).to_contain_text(
        "Read ", timeout=10_000
    )


def matching_row(filename: str | None, **overrides: str) -> list[str]:
    fields = dict(MATCHING_FIELDS)
    fields.update(overrides)
    return [filename or "", fields["brand"], fields["class_type"], fields["abv"],
            fields["net_contents"], fields["producer"], fields["origin_country"],
            fields["is_import"]]


XLSX_HEADER = ["filename", "brand", "class_type", "abv", "net_contents",
               "producer", "origin_country", "is_import"]


class TestXlsxEndToEnd:
    def test_xlsx_ingests_previews_and_scans_to_a_matched_worksheet(self, page, base_url):
        page.goto(base_url + "/")
        page.set_input_files("#file-input", files=memory_files(["a.png", "b.png"]))
        page.set_input_files("#csv-input", files=xlsx_payload([
            XLSX_HEADER, matching_row("a.png"), matching_row("b.png"),
        ]))
        # The status line reports the parse and the preview lets the agent
        # eyeball the rows BEFORE scanning.
        playwright_api.expect(page.locator("#csv-status")).to_contain_text(
            "Read 2 rows from “submittal.xlsx”"
        )
        preview = page.locator("#ingest-preview")
        playwright_api.expect(preview).to_be_visible()
        preview.locator("summary").click()
        playwright_api.expect(preview).to_contain_text("Stone's Throw")

        page.click("#scan-button")
        playwright_api.expect(page.locator("#banner-text")).to_have_text(
            "2 labels scanned — 2 passed", timeout=15_000
        )
        playwright_api.expect(worksheet_rows(page)).to_have_count(2)
        playwright_api.expect(worksheet_rows(page).first).to_contain_text("a.png")
        playwright_api.expect(
            worksheet_rows(page).first.locator('td[data-label="Score"]')
        ).to_have_text("6/6 fields match")


