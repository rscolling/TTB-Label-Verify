class TestRequiredElements:
    """Per-family required-elements check (TTB-cited; REVIEW severity)."""

    def scan_one(self, page, base_url, manifest_row: list[str], extraction) -> None:
        page.goto(base_url + "/")
        page.set_input_files("#file-input", files=memory_files(["label.png"]))
        header = ",".join(XLSX_HEADER)
        line = ",".join('"' + cell.replace('"', '""') + '"' for cell in manifest_row)
        page.set_input_files("#csv-input", files=csv_payload(f"{header}\n{line}\n"))
        page.click("#scan-button")
        wait_for_banner(page)

    def test_spirits_missing_abv_is_review_with_field_of_vision_reason(
        self, page, base_url, extractor, tmp_path
    ):
        # Submittal doesn't declare ABV (blank -> server verdict "na") and the
        # photo shows none: spirits require it (27 CFR 5.65), same field of
        # vision as brand and class (5.61) -> REVIEW, specially worded reason.
        extractor.delegate = FakeExtractor(replace(GOOD_EXTRACTION, alcohol_content=None))
        self.scan_one(page, base_url, matching_row("label.png", abv=""), None)

        row = worksheet_rows(page).first
        playwright_api.expect(row.locator(".status-badge")).to_have_text("REVIEW")
        playwright_api.expect(row.locator(".flag")).to_have_count(1)
        playwright_api.expect(
            row.locator('td[data-label="Alcohol content"] .mark')
        ).to_have_attribute("title", "Required element — not found in this photo")

        row.locator(".review-button").click()
        block = page.locator(".required-missing")
        playwright_api.expect(block).to_be_visible()
        playwright_api.expect(block).to_contain_text("Required on every label")
        playwright_api.expect(block).to_contain_text(
            "same field of vision as the brand and class (27 CFR 5.61)"
        )
        page.keyboard.press("Escape")

        # Export: rides the existing verdict/reason columns, no new columns.
        with page.expect_download() as download_info:
            page.click("#download-csv")
        path = tmp_path / "required.csv"
        download_info.value.save_as(path)
        rows = list(csv.reader(io.StringIO(path.read_bytes().decode("utf-8-sig"))))
        header = rows[0]
        assert rows[1][header.index("pass_fail")] == "REVIEW"
        assert rows[1][header.index("abv_verdict")] == "missing_required"
        assert "27 CFR 5.61" in rows[1][header.index("abv_reason")]
        assert header[-1] == "error"  # column contract intact

    def test_malt_missing_abv_is_not_flagged(self, page, base_url, extractor):
        # Malt beverages: alcohol content is conditional (27 CFR 7.63(a)(3)),
        # so its absence must NOT flag the row.
        extractor.delegate = FakeExtractor(replace(
            GOOD_EXTRACTION, class_type="India Pale Ale", alcohol_content=None
        ))
        self.scan_one(
            page, base_url,
            matching_row("label.png", class_type="India Pale Ale", abv=""),
            None,
        )
        row = worksheet_rows(page).first
        playwright_api.expect(row.locator(".status-badge")).to_have_text("PASS")
        playwright_api.expect(row.locator(".flag")).to_have_count(0)

    def test_wine_missing_abv_is_review_with_generic_reason(self, page, base_url, extractor):
        extractor.delegate = FakeExtractor(replace(
            GOOD_EXTRACTION, class_type="Red Wine", alcohol_content=None
        ))
        self.scan_one(
            page, base_url,
            matching_row("label.png", class_type="Red Wine", abv=""),
            None,
        )
        row = worksheet_rows(page).first
        playwright_api.expect(row.locator(".status-badge")).to_have_text("REVIEW")
        row.locator(".review-button").click()
        block = page.locator(".required-missing")
        playwright_api.expect(block).to_contain_text("Alcohol content")
        playwright_api.expect(block).to_contain_text(
            "may appear on another label or be embossed on the container"
        )
        page.keyboard.press("Escape")

    def test_missing_required_element_never_downgrades_a_real_fail(
        self, page, base_url, extractor
    ):
        # Wrong brand (FAIL) + missing net contents (required): stays FAIL.
        extractor.delegate = FakeExtractor(replace(
            GOOD_EXTRACTION, brand="Completely Different", net_contents=None
        ))
        self.scan_one(page, base_url, matching_row("label.png", net_contents=""), None)
        row = worksheet_rows(page).first
        playwright_api.expect(row.locator(".status-badge")).to_have_text("FAIL")
        row.locator(".review-button").click()
        playwright_api.expect(page.locator(".required-missing")).to_contain_text(
            "Amount in bottle"
        )
        page.keyboard.press("Escape")

    def test_no_submittal_scan_still_flags_missing_required_elements(
        self, page, base_url, extractor
    ):
        # No form at all: the required-elements check is label-intrinsic and
        # still runs off the extraction (spirits family from the extracted
        # class/type; ABV missing -> flagged in the drill-down).
        extractor.delegate = FakeExtractor(replace(GOOD_EXTRACTION, alcohol_content=None))
        page.goto(base_url + "/")
        page.set_input_files("#file-input", files=memory_files(["label.png"]))
        page.click("#scan-button")
        wait_for_banner(page)
        row = worksheet_rows(page).first
        playwright_api.expect(row.locator(".status-badge")).to_have_text("REVIEW")
        row.locator(".review-button").click()
        playwright_api.expect(page.locator(".required-missing")).to_contain_text(
            "Alcohol content"
        )
        page.keyboard.press("Escape")
