def tiny_png() -> bytes:
    buffer = io.BytesIO()
    Image.new("RGB", (2, 2), "white").save(buffer, format="PNG")
    return buffer.getvalue()


PNG = tiny_png()


def memory_files(names: list[str]) -> list[dict]:
    return [{"name": name, "mimeType": "image/png", "buffer": PNG} for name in names]


def xlsx_payload(rows: list[list[object]], name: str = "submittal.xlsx") -> list[dict]:
    workbook = openpyxl.Workbook()
    for row in rows:
        workbook.active.append(row)
    buffer = io.BytesIO()
    workbook.save(buffer)
    return [{
        "name": name,
        "mimeType": "application/vnd.openxmlformats-officedocument.spreadsheetml.sheet",
        "buffer": buffer.getvalue(),
    }]


def csv_payload(text: str, name: str = "submittal.csv") -> list[dict]:
    return [{"name": name, "mimeType": "text/csv", "buffer": text.encode("utf-8")}]


def pdf_payload(name: str = "submittal.pdf") -> list[dict]:
    # Real magic bytes; the content never reaches a model — the fake answers.
    return [{"name": name, "mimeType": "application/pdf", "buffer": b"%PDF-1.4 fake form"}]


